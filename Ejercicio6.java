public class Ejercicio6 {
    public static void main(String[] args) {
        //Ejercicio 6: ¿Qué precio tenían antes del descuento?
        double precioConDescuento = 34.0;
        double descuento = 0.15;
        
        // Precio inicial del pantalon
        double precioInicial = precioConDescuento / -- descuento;

        System.out.printf("El precio inicial de los pantalones era: %s", precioInicial);
    }
}
